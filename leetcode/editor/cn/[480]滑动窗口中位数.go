func medianSlidingWindow(nums []int, k int) []float64 {
	if k == 1 {
		ans := make([]float64, len(nums))
		for i, num := range nums {
			ans[i] = float64(num)
		}
		return ans
	}

	n := len(nums)
	ans := make([]float64, 0)
	left, right := 0, k-1
	window := make([]int, k)

	for right < n {
		// æŠŠæ•°æ”¾è¿›çª—å£ä¸­
		// æ±‚ left ~ right é—´çš„ä¸­ä½æ•°
		// ç§»åŠ¨çª—å£
		copy(window, nums[left:right+1])
		// æ’åºä¼šè¶…æ—¶ï¼Œæ­¤æ–¹æ³•ä¸è¡Œ
		// TODO ç”¨å †ã€ä¼˜å…ˆé˜Ÿåˆ—
		sort.Ints(window)

		ans = append(ans, getMedian(window, k))
		left++
		right++
	}
	return ans
}
func getMedian(winNums []int, k int) float64 {
	d := k / 2
	if k%2 == 0 {
		return (float64(winNums[d-1]) + float64(winNums[d])) / 2
	} else {
		return float64(winNums[d])
	}
}

