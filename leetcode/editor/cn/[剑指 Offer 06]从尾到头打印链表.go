/**
 * Definition for singly-linked list.
 * type ListNode struct {
 *     Val int
 *     Next *ListNode
 * }
 */
func reversePrint(head *ListNode) []int {
	node := head
	cnt := 0
	for node != nil {
		cnt++
		node = node.Next
	}
	node = head
	ret := make([]int, cnt)
	for i := cnt-1; i>=0;i-- {
		ret[i] = node.Val
		node = node.Next
	}
	return ret
}
