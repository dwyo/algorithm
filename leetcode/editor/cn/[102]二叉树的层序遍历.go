/**
 * Definition for a binary tree node.
 * type TreeNode struct {
 *     Val int
 *     Left *TreeNode
 *     Right *TreeNode
 * }
 */
func levelOrder(root *TreeNode) [][]int {
	if root == nil {
		return [][]int{}
	}
	ret := make([][]int, 0)
	queue := []*TreeNode{root}
	for len(queue) > 0 {
		n := len(queue)
		s := make([]int, 0)
		for n > 0 {
			n--
			node := queue[0]
			if node.Left != nil {
				queue = append(queue, node.Left)
			}
			if node.Right != nil {
				queue = append(queue, node.Right)
			}
			s = append(s, node.Val)
			queue = queue[1:]
		}
		ret = append(ret, s)
	}
	return ret
}

