func addToArrayForm(A []int, K int) []int {
	n := len(A)
	if K == 0 {
		return A
	}
	ret := make([]int, 0)
	reverse(A)
	m := 0
	for i := 0; i < n; i++ {
		m = K % 10
		K /= 10
		sum := m + A[i]
		if sum >= 10 {
			K++
			sum -= 10
		}
		ret = append(ret, sum)
	}
	for ; K > 0; K /= 10 {
		ret = append(ret, K%10)
	}
	reverse(ret)
	return ret
}

func reverse(A []int) {
	for i, n := 0, len(A); i < n/2; i++ {
		A[i], A[n-1-i] = A[n-1-i], A[i]
	}
}

